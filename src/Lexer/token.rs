#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    Package,
    Import,
    Fn,
    Var,
    Const,
    If,
    Else,
    For,
    While,
    Switch,
    Case,
    Default,
    Return,
    Break,
    Continue,
    Extern,
    Struct,

    Identifier(String),
    IntLiteral(i64),
    FloatLiteral(f64),
    StringLiteral(String),

    TypeInt,
    TypeFloat,
    TypeString,
    TypeBool,
    TypeVoid,

    True,
    False,

    Plus,
    Minus,
    PlusPlus,
    MinusMinus,
    Star,
    Slash,
    Percent,
    Equal,
    ColonEqual,
    Bang,
    EqualEqual,
    NotEqual,
    Less,
    LessEqual,
    Greater,
    GreaterEqual,
    Ampersand,
    Pipe,
    Caret,
    Tilde,
    AndAnd,
    PipePipe,

    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    LeftBracket,
    RightBracket,
    Comma,
    Semicolon,
    Dot,
    Colon,

    EOF,
}

#[derive(Debug, Clone)]
pub struct TokenWithSpan {
    pub token: Token,
    pub line: usize,
}
